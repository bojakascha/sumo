import sharp from 'sharp';
import { writeFileSync } from 'fs';

const SRC = 'images/arena_2_mask.webp';
const OUT = 'src/game/arenaCollisionData.js';
const W = 256;
const H = 384;

// 2-bit zone values
const ZONE_ALLOWED = 0; // transparent (alpha < 128)
const ZONE_WALL    = 1; // white (opaque, high brightness)
const ZONE_DEATH   = 2; // red (opaque, r high, g+b low)

async function main() {
  const { data } = await sharp(SRC)
    .resize(W, H, { fit: 'fill' })
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  // 2 bits per pixel, 4 pixels per byte
  const totalPixels = W * H;
  const bytes = new Uint8Array(Math.ceil(totalPixels / 4));

  for (let i = 0; i < totalPixels; i++) {
    const pi = i * 4;
    const r = data[pi];
    const g = data[pi + 1];
    const b = data[pi + 2];
    const a = data[pi + 3];

    let zone;
    if (a < 128) {
      zone = ZONE_ALLOWED;
    } else if (r > 180 && g < 100 && b < 100) {
      zone = ZONE_DEATH;
    } else if (r > 180 && g > 180 && b > 180) {
      zone = ZONE_WALL;
    } else {
      zone = ZONE_ALLOWED;
    }

    bytes[i >> 2] |= (zone << ((i & 3) << 1));
  }

  const b64 = Buffer.from(bytes).toString('base64');

  const js = `// Auto-generated by scripts/process-arena-mask.js — do not edit
// 2 bits per pixel: 0=allowed, 1=wall (bounce), 2=death
export const COLLISION_W = ${W};
export const COLLISION_H = ${H};
export const COLLISION_DATA = Uint8Array.from(atob("${b64}"), c => c.charCodeAt(0));
`;

  writeFileSync(OUT, js);
  console.log(`Written ${OUT}: ${W}x${H} grid, ${bytes.length} bytes (2bpp)`);
}

main();
